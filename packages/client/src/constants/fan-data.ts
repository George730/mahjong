// Phase 2F: Complete data for all 81 国标麻将 fan types.
// Each entry includes score, Chinese name, explanation, and an example hand
// using compact notation: Xm=万, Xp=筒, Xs=条, E/S/W/N=风, Z/F/B=箭.
// Groups within a hand are separated by "|". Tiles within a group by spaces.
export interface Example {
  tiles: string;
  desc: string;
}

export interface FanEntry {
  name: string;
  score: number;
  explanation: string;
  /** Example hand in compact notation. "|" separates melds/pairs. */
  example?: Example[];
}

export interface FanGroup {
  score: number;
  fans: FanEntry[];
}

const e = (tiles: string, desc: string): Example => ({
  tiles,
  desc,
});

const f = (name: string, score: number, explanation: string, example?: Example[]): FanEntry => ({
  name,
  score,
  explanation,
  example,
});

